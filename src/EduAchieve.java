public class EduAchieve{

    String eduAchieve;
    String schoolName;
    int year;
    String degree;
    String major;

    public void EduAchieve(){













    }

    public EduAchieve(String eduAchieve, String schoolName, int year, String degree, String major) {
        this.eduAchieve = eduAchieve;
        this.schoolName = schoolName;
        this.year = year;
        this.degree = degree;
        this.major = major;
    }










    /*******************************************getters/setters***********************************/
    public String getEduAchieve() {
        return eduAchieve;
    }

    public void setEduAchieve(String eduAchieve) {
        this.eduAchieve = eduAchieve;
    }

    public String getSchoolName() {
        return schoolName;
    }

    public void setSchoolName(String schoolName) {
        this.schoolName = schoolName;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }
}
